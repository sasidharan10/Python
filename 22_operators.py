# Identity Operators
# Membership Operators
# Bitwise Operators

# Arithmetic Operators
print("\n------Arithmetic Operators------\n")
print("5 + 6 is ", 5+6)
print("5 - 6 is ", 5-6)
print("5 * 6 is ", 5*6)
print("5 / 6 is ", 5/6)
print("5 ** 3 is (5^3)", 5**3)
print("5 % 5 is ", 5 % 5)
print("15 // 6 is (floor value)", 15//6)

# Assignment Operators
print("\n------Assignment Operators------\n")
x = 5
print(x)
x %= 7  # x = x%7
print(x)

# Comparison Operators
print("\n------Comparison Operators------\n")
print("5==3 :", 5 == 3)
print("5!=3 :", 5 != 3)
print("5<=3 :", 5 <= 3)
print("5>=3 :", 5 >= 3)


# Logical Operators
print("\n------Logical Operators------\n")
a = True
b = False
print("a and b :", a and b)
print("a or b :", a or b)

# Identity Operators
print("\n------Identity Operators------\n")
print("5 is 5 :", 5 is 5)
print("5 is not 5 :", 5 is not 5)

# Membership Operators
print("\n------Membership Operators------\n")
list = [3, 3, 2, 2, 39, 33, 35, 32]
print("324 in list :", 324 in list)
print("324 not in list :", 324 not in list)

# Bitwise Operators
# 0 - 00
# 1 - 01
# 2 - 10
# 3 - 11
print("\n------Bitwise Operators------\n")
print("0 | 1 :", 0 | 1)
print("0 | 2 :", 0 | 2)
print("0 & 2 :", 0 & 2)
print("0 & 3 :", 0 & 3)

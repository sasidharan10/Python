name = 'sasidharan'

print("printing name :", name)
print("name[2] :", name[2])
print("name[-1] :", name[-1])
print("name[0:5] :", name[0:5])
print("name[4:15] :", name[4:15])
print("name[:10] :", name[:10])  # start is not given
print("name[4:] :", name[4:])   # end is not given
print("name[:] :", name[:])   # prints all characters
print("name[::] :", name[::])   # prints all characters
print("reverse : ", name[::-1])
print("reverse(-2) : ", name[::-2])
# prints all characters in the given range with gaps mentioned
print("name[0:10:2] :", name[0:10:2])

name = 'doll'
# name[2]='k' strings are immutable
print("name (changed) :", name)
print("len(name) :", len(name))

print("reverse : ", name[::-1])

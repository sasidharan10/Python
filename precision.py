# format specifiers :

"""

%d for integers
%f for floating-point numbers
%b for binary numbers
%o for octal numbers
%x for octal hexadecimal numbers
%s for string
%e for floating-point in an exponent format

"""

print("\nInteger : %d"% 10)
print("Float : %.3f"% 5.62446)
print("String : %s"% "Messi")
